function hao($len = 18){
		$str = "0123456789";
		$strlen = strlen($str);
		$randstr = "";
		for ($i = 0; $i < $len; $i++) {
			$randstr .= $str[mt_rand(0, $strlen - 1)];
		}
		return $randstr;
}

